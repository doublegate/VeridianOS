void VeridianWindowManagementInterface::bind(struct wl_client *client,
                                              void *data,
                                              uint32_t version, uint32_t id)
{
    auto *self = static_cast<VeridianWindowManagementInterface *>(data);

    struct wl_resource *resource = wl_resource_create(client,
        &org_kde_plasma_window_management_interface,
        static_cast<int>(qMin(version, 16u)), id);
    wl_resource_set_implementation(resource, nullptr, self, nullptr);

    self->m_boundResources.append(resource);

    /* Send existing windows to newly bound client */
    for (auto it = self->m_windows.begin(); it != self->m_windows.end(); ++it) {
        self->sendWindowToClient(resource, it.value());
    }
}

void VeridianWindowManagementInterface::sendWindowToClient(
    struct wl_resource *managerResource, const ManagedWindowData &window)
{
    /* Create a per-window resource for this client */
    struct wl_client *client = wl_resource_get_client(managerResource);
    struct wl_resource *windowResource = wl_resource_create(client,
        &org_kde_plasma_window_interface,
        wl_resource_get_version(managerResource), 0);

    /* Set up per-window callbacks */
    static const struct org_kde_plasma_window_interface s_windowImpl = {
        VeridianWindowManagementInterface::windowSetState,
        VeridianWindowManagementInterface::windowClose,
        VeridianWindowManagementInterface::windowRequestMove,
        VeridianWindowManagementInterface::windowRequestResize,
        VeridianWindowManagementInterface::windowRequestVirtualDesktop,
    };

    /* Store internal ID in user data for callback routing */
    uint32_t *idPtr = new uint32_t(window.internalId);
    wl_resource_set_implementation(windowResource, &s_windowImpl,
                                   idPtr, windowDestroy);

    /* Send the window announcement */
    org_kde_plasma_window_management_send_window(managerResource,
                                                  window.internalId);

    /* Send window properties */
    org_kde_plasma_window_send_title_changed(windowResource,
                                              window.title.toUtf8().constData());
    org_kde_plasma_window_send_app_id_changed(windowResource,
                                               window.appId.toUtf8().constData());
    org_kde_plasma_window_send_state_changed(windowResource, window.state);
    org_kde_plasma_window_send_pid_changed(windowResource, window.pid);

    if (window.geometry.isValid()) {
        org_kde_plasma_window_send_geometry(windowResource,
            window.geometry.x(), window.geometry.y(),
            window.geometry.width(), window.geometry.height());
    }

    /* Send virtual desktop membership */
    for (const QString &desktopId : window.virtualDesktopIds) {
        org_kde_plasma_window_send_virtual_desktop_entered(
            windowResource, desktopId.toUtf8().constData());
    }
}

void VeridianWindowManagementInterface::addWindow(uint32_t internalId,
                                                   const QString &title,
                                                   const QString &appId,
                                                   uint32_t pid)
{
    ManagedWindowData window;
    window.internalId = internalId;
    window.windowResource = nullptr;
    window.title = title;
    window.appId = appId;
    window.pid = pid;
    window.state = 0;
    window.closeable = true;
    window.movable = true;
    window.resizable = true;
    window.maximizable = true;
    window.minimizable = true;
    window.fullscreenable = true;

    m_windows.insert(internalId, window);

    /* Notify all bound clients */
    for (struct wl_resource *res : m_boundResources) {
        sendWindowToClient(res, window);
    }
}

void VeridianWindowManagementInterface::removeWindow(uint32_t internalId)
{
    m_windows.remove(internalId);
    /* Clients receive the unmapped event via the per-window resource destroy */
}

