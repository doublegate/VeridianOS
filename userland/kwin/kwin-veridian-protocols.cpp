void VeridianWindowManagementInterface::setShowDesktop(bool show)
{
    for (struct wl_resource *res : m_boundResources) {
        org_kde_plasma_window_management_send_show_desktop_changed(
            res, show ? 1 : 0);
    }
}

void VeridianWindowManagementInterface::updateStackingOrder(
    const QVector<uint32_t> &order)
{
    /* Create wl_array from stacking order */
    struct wl_array arr;
    wl_array_init(&arr);

    for (uint32_t id : order) {
        uint32_t *ptr = static_cast<uint32_t *>(wl_array_add(&arr, sizeof(uint32_t)));
        if (ptr)
            *ptr = id;
    }

    for (struct wl_resource *res : m_boundResources) {
        org_kde_plasma_window_management_send_stacking_order_changed(res, &arr);
    }

    wl_array_release(&arr);
}

void VeridianWindowManagementInterface::windowSetState(
    struct wl_client *, struct wl_resource *resource,
    uint32_t flags, uint32_t state)
{
    uint32_t *idPtr = static_cast<uint32_t *>(wl_resource_get_user_data(resource));
    if (!idPtr)
        return;

    /* Decode state change and emit appropriate signal.
     * Active bit = 0x001, Minimized = 0x002, Maximized = 0x004 */
    if (flags & 0x001) {
        /* Activate request */
        /* Signal handled by caller */
    }
    if (flags & 0x002) {
        /* Minimize/unminimize */
    }
    if (flags & 0x004) {
        /* Maximize/unmaximize */
    }

    Q_UNUSED(state);
}

