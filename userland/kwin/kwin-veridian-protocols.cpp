bool VeridianOutputManagement::applyConfiguration(
    const QVector<OutputConfigChange> &changes)
{
    if (!m_backend)
        return false;

    for (const OutputConfigChange &change : changes) {
        /* Find the output for this change */
        for (VeridianDrmOutput *output : m_backend->outputs()) {
            if (output->connectorId() == change.outputId) {
                if (change.modeIndex >= 0) {
                    QVector<drmModeModeInfo> modes = output->availableModes();
                    if (change.modeIndex < modes.size()) {
                        output->setMode(modes[change.modeIndex]);
                    }
                }
                /* Additional changes (transform, scale, position) would
                 * be applied here in a full implementation */
                break;
            }
        }
    }

    Q_EMIT configurationChangeRequested(changes);
    return true;
}

