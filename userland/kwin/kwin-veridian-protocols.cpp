bool VeridianProtocolRegistry::initialize()
{
    bool gpuAccelerated = true;
    if (m_backend && m_backend->eglBackend()) {
        gpuAccelerated = !m_backend->eglBackend()->isLlvmpipe();
    }

    m_plasmaShell = new VeridianPlasmaShellInterface(m_display, this);
    m_windowManagement = new VeridianWindowManagementInterface(m_display, this);
    m_serverDecoration = new VeridianServerDecorationManager(m_display, this);
    m_blurManager = new VeridianBlurManager(m_display, gpuAccelerated, this);
    m_dpmsManager = new VeridianDpmsManager(m_display, m_backend, this);
    m_outputDevice = new VeridianOutputDeviceInterface(m_display, this);
    m_outputManagement = new VeridianOutputManagement(m_display, m_backend, this);

    /* Register outputs from DRM backend into output device protocol */
    if (m_backend) {
        for (VeridianDrmOutput *output : m_backend->outputs()) {
            OutputDeviceData data;
            data.id = output->connectorId();
            data.manufacturer = QStringLiteral("VeridianOS");
            data.model = output->name();
            data.serialNumber = QStringLiteral("VIRT-001");
            data.physicalSize = output->sizeMillimeters();
            data.globalPosition = QPoint(0, 0);
            data.currentModeIndex = 0;
            data.preferredModeIndex = 0;
            data.transform = 0;
            data.scale = 1000;  /* 1.0x in fixed-point */
            data.enabled = true;
            data.connectorId = output->connectorId();

            /* Copy modes from DRM output */
            data.modes = output->availableModes();

            m_outputDevice->addOutput(data);
        }
    }

    qDebug("VeridianProtocolRegistry: all KDE Wayland protocols registered "
           "(blur %s)", gpuAccelerated ? "enabled" : "disabled");

    return true;
}

