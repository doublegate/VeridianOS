void VeridianOutputDeviceInterface::bind(struct wl_client *client, void *data,
                                          uint32_t version, uint32_t id)
{
    auto *self = static_cast<VeridianOutputDeviceInterface *>(data);

    struct wl_resource *resource = wl_resource_create(client,
        &org_kde_kwin_outputdevice_interface,
        static_cast<int>(qMin(version, 2u)), id);
    wl_resource_set_implementation(resource, nullptr, self, nullptr);

    self->m_boundResources.append(resource);

    /* Send all known outputs to the newly bound client */
    for (const OutputDeviceData &output : self->m_outputs) {
        self->sendOutputData(resource, output);
    }
}

void VeridianOutputDeviceInterface::sendOutputData(
    struct wl_resource *resource, const OutputDeviceData &data)
{
    org_kde_kwin_outputdevice_send_geometry(resource,
        data.globalPosition.x(), data.globalPosition.y(),
        data.physicalSize.width(), data.physicalSize.height(),
        0, /* subpixel: unknown */
        data.manufacturer.toUtf8().constData(),
        data.model.toUtf8().constData(),
        data.transform);

    /* Send modes */
    for (int i = 0; i < data.modes.size(); ++i) {
        bool current = (i == data.currentModeIndex);
        bool preferred = (i == data.preferredModeIndex);
        sendMode(resource, data.modes[i], i, current, preferred);
    }

    org_kde_kwin_outputdevice_send_scale(resource, data.scale);
    org_kde_kwin_outputdevice_send_enabled(resource, data.enabled ? 1 : 0);
    org_kde_kwin_outputdevice_send_serial_number(resource,
        data.serialNumber.toUtf8().constData());
    org_kde_kwin_outputdevice_send_done(resource);
}

void VeridianOutputDeviceInterface::sendMode(
    struct wl_resource *resource, const drmModeModeInfo &mode,
    int index, bool current, bool preferred)
{
    uint32_t flags = 0;
    if (current)
        flags |= 0x1;    /* WL_OUTPUT_MODE_CURRENT */
    if (preferred)
        flags |= 0x2;    /* WL_OUTPUT_MODE_PREFERRED */

    org_kde_kwin_outputdevice_send_mode(resource,
        flags, mode.hdisplay, mode.vdisplay,
        mode.vrefresh * 1000, index);
}

void VeridianOutputDeviceInterface::addOutput(const OutputDeviceData &data)
{
    m_outputs.append(data);
    for (struct wl_resource *res : m_boundResources) {
        sendOutputData(res, data);
    }
}

void VeridianOutputDeviceInterface::removeOutput(uint32_t id)
{
    for (int i = 0; i < m_outputs.size(); ++i) {
        if (m_outputs[i].id == id) {
            m_outputs.removeAt(i);
            break;
        }
    }
}

void VeridianOutputDeviceInterface::updateOutput(const OutputDeviceData &data)
{
    for (int i = 0; i < m_outputs.size(); ++i) {
        if (m_outputs[i].id == data.id) {
            m_outputs[i] = data;
            for (struct wl_resource *res : m_boundResources) {
                sendOutputData(res, data);
            }
            break;
        }
    }
}

QVector<OutputDeviceData> VeridianOutputDeviceInterface::outputs() const
{
    return m_outputs;
}

